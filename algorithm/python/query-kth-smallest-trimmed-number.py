# https://leetcode.com/problems/query-kth-smallest-trimmed-number/description/

from typing import List


def digit_of(target: str, position: int) -> str:
    return target[len(target) - 1 - position]


def counting_sort(arr: list, position: int) -> list:
    # complexities of time, space: O(N), O(N)
    if len(arr) <= 0:
        return arr

    counts = {str(k): 0 for k in range(10)}
    for item in arr:  # Ot(N)
        digit = digit_of(item[1], position)
        counts[digit] += 1

    index = {str(k): 0 for k in range(10)}
    for idx in "0123456789":
        index[str(int(idx) + 1)] = counts[idx] + index[idx]

    ret = [""] * len(arr)  # Os(N)
    for idx, val in arr:  # Ot(N)
        digit = digit_of(val, position)
        ret[index[digit]] = (idx, val)
        index[digit] += 1

    return ret


class Solution:
    def smallestTrimmedNumbers(
        self, nums: List[str], queries: List[List[int]]
    ) -> List[int]:
        lookup = []

        nums = [(idx, val) for idx, val in enumerate(nums)]

        for pos in range(len(nums[0][1])):
            nums = counting_sort(nums, pos)
            lookup = [*lookup, nums]

        return [lookup[trim - 1][rank - 1][0] for rank, trim in queries]


